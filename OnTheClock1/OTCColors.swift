import Foundation

extension UIColor {
    
    class func OTCDark() -> UIColor {
        return UIColor(red:238.0/255.0, green:51.0/255.0, blue:232.0/255.0, alpha:1.0)
    }
    class func OTCLight() -> UIColor {
        return UIColor(red:255/255.0, green:255/255.0, blue:255/255.0, alpha:1.0)
    }
    class func OTCLightGray() -> UIColor {
        return UIColor(red:245/255.0, green:245/255.0, blue:245/255.0, alpha:1.0)
    }
    
}
